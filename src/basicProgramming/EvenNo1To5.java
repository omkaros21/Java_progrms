package basicProgramming;

public class EvenNo1To5 {
	//WAP to print all the even no. in the range 1-5
	
	    public static void main(String[] args) {
	        for(int i = 1;i<=5;i++)
	        {
	            if(i%2==0)
	            {
	                System.out.println(i);
	            }
	        }
	    }
	
}
