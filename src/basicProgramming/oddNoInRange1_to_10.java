package basicProgramming;

public class oddNoInRange1_to_10 {

	//WAP to print all the odd no. in the range 1-10
	
	    public static void main(String[] args) {
	        for(int i=1;i<=10;i++)
	        {
	            if(i%2!=0)
	            {
	                System.out.println(i);
	            }
	        }
	    }
}
